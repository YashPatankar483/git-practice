
num1 = int(input("Enter any number: "))
num2 = int(input("Enter any number: "))

def add(num1, num2):
    return num1 + num2

def multiply(num1, num2):
    return num1 * num2

def div(num1, num2):
    return num1 / num2

def modulo(num1, num2):
    return num1 % num2

print(add(num1,num2))
print(multiply(num1,num2))
print(div(num1,num2))
print(modulo(num1,num2))